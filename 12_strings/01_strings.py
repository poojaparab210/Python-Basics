# Strings are one of the most fundamental data types in Python. A string is asequence of characters enclosed within either single quotes (' ), double quotes(" ), or triple quotes (''' or “““).

# Double-quoted string
name = "Pooja"

# Single-quoted string
name = 'Pooja'

# Triple-quoted string (useful for multi-line strings)
name = '''Pooja is 
a new learner'''
print(name)
