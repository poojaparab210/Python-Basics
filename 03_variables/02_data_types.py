# Data Types in Python:
# 1. Integers(int): Whole nos. for example 1, -6   
age = 3
print(age)
print(type(age))

# 2. Floats(float): Decimal nos. for ex., 3.14 , 1.0
cgpa = 8.2
print(cgpa)
print(type(cgpa))

# 3. Strings(str): Text data enclosed in quotes for ex., "Hello", "Pooja" 
name = "Pooja"
print(name)
print(type(name))

# 4. Booleans(bool): Represents True or False
is_completed = True
print(is_completed)
print(type(is_completed))
