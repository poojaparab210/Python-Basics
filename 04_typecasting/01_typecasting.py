# Typecasting is the process of converting one data type to another.
# Python provides build-in functions for typecasting.
# 1. int(): Converts to integers.
# 2. float(): Converts to float.
# 3. str(): Converts to string.
# 4. bool(): Converts to boolean.
a = 34
b = "34"
c = 34.6
d = 223

print(a)
print(type(a))

print(b)
print(type(b))

#Convert string(b) to an integer
c = int(b)
print(c)
print(type(c))

#Convert integer(d) to a string
e = str(d)
print(e)
print(type(e))

#Convert float(c) to a integer
f = int(c)
print(f)
print(type(f))



