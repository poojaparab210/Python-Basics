# Assignment Operators:

a = 32
print(a)

a+=3        # it will add 3 to which is give in 'a = 32'.
print(a)

a-=4    # it will subtract 4 from the current value of 'a'.
print(a)

a*=2    # it will multiply 2 with the current value of 'a'.
print(a)

a/=2    # it will divide 2 with the current value of 'a'.
print(a)

a%=2    # it will give the remainder of the current value of 'a' when divided by 2.
print(a)

a//=2   # it will divide 2 with the current value of 'a' and give the floor value.
print(a)