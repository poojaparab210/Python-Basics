# Escape Sequences:
# Escape sequences are used to include special characters in strings.
# Common escape sequences:
# \n : Newline
# \t : Tab
# \\ : Backslash
# \" : Double quote
# \' : Single quote

print("Hey how  are you?\nI am good.")
print("Python is a programming language\tIt is easy to learn.")
print("This is a backslash: \\")
print("\"Python is awesome!\"")
print("Hello, I\'m learning Python.")